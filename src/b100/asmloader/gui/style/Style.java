package b100.asmloader.gui.style;

public class Style {
	
	private String name;
	private String className;
	
	public Style(String name, String className) {
		this.name = name;
		this.className = className;
	}
	
	public String getName() {
		return name;
	}
	
	public String getClassName() {
		return className;
	}

}
